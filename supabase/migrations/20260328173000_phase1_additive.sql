CREATE OR REPLACE VIEW public.sales_enriched_v AS
WITH source_rows AS (
    SELECT
        s.id AS sales_row_id,
        s.transaction_date,
        s.transaction_date AS sale_date,
        s.store_name,
        s.product_name,
        COALESCE(s.quantity, 0) AS quantity,
        COALESCE(s.total_amount, 0)::NUMERIC(12, 2) AS total_amount,
        COALESCE(s.total_amount, 0)::NUMERIC(12, 2) AS sales_amount,
        s.created_at
    FROM public.product_sales_data s
),
matched AS (
    SELECT
        sr.*,
        alias_match.product_id AS alias_product_id,
        direct_match.id AS direct_product_id
    FROM source_rows sr
    LEFT JOIN LATERAL (
        SELECT pa.product_id
        FROM public.product_aliases pa
        WHERE pa.alias_name = sr.product_name
          AND pa.source_system = 'pos'
          AND pa.is_active = TRUE
        ORDER BY pa.updated_at DESC NULLS LAST, pa.id DESC
        LIMIT 1
    ) alias_match ON TRUE
    LEFT JOIN LATERAL (
        SELECT p.id
        FROM public.products p
        WHERE p.product_name = sr.product_name
        ORDER BY p.updated_at DESC NULLS LAST, p.id DESC
        LIMIT 1
    ) direct_match ON TRUE
)
SELECT
    m.sales_row_id,
    m.transaction_date,
    m.sale_date,
    m.store_name,
    m.product_name,
    m.quantity,
    m.total_amount,
    m.sales_amount,
    p.id AS matched_product_id,
    p.jan_code,
    COALESCE(p.category, '未分類') AS category,
    p.product_group,
    p.brand,
    p.cost_price::NUMERIC(12, 2) AS cost_price,
    p.selling_price::NUMERIC(12, 2) AS selling_price,
    (COALESCE(p.cost_price, 0) * m.quantity)::NUMERIC(12, 2) AS estimated_cost,
    (
        m.sales_amount
        - (COALESCE(p.cost_price, 0) * m.quantity)
    )::NUMERIC(12, 2) AS estimated_profit,
    CASE
        WHEN m.alias_product_id IS NOT NULL THEN 'alias'
        WHEN m.direct_product_id IS NOT NULL THEN 'direct_name'
        ELSE 'unmatched'
    END AS match_source,
    (COALESCE(m.alias_product_id, m.direct_product_id) IS NULL) AS unmatched_master,
    m.created_at
FROM matched m
LEFT JOIN public.products p
    ON p.id = COALESCE(m.alias_product_id, m.direct_product_id);

CREATE OR REPLACE VIEW public.sales_daily_summary_v AS
SELECT
    sale_date,
    transaction_date,
    store_name,
    SUM(quantity) AS total_quantity,
    SUM(sales_amount)::NUMERIC(12, 2) AS total_sales_amount,
    COUNT(*) AS line_count,
    COUNT(DISTINCT product_name) AS product_count,
    SUM(estimated_cost)::NUMERIC(12, 2) AS estimated_cost,
    SUM(estimated_profit)::NUMERIC(12, 2) AS estimated_profit
FROM public.sales_enriched_v
GROUP BY sale_date, transaction_date, store_name;

CREATE OR REPLACE VIEW public.sales_product_summary_v AS
SELECT
    sale_date,
    transaction_date,
    store_name,
    matched_product_id AS product_id,
    COALESCE(jan_code, '-') AS jan_code,
    product_name,
    COALESCE(category, '未分類') AS category,
    product_group,
    brand,
    SUM(quantity) AS total_quantity,
    SUM(sales_amount)::NUMERIC(12, 2) AS total_sales_amount,
    SUM(estimated_cost)::NUMERIC(12, 2) AS estimated_cost,
    SUM(estimated_profit)::NUMERIC(12, 2) AS estimated_profit,
    BOOL_OR(unmatched_master) AS unmatched_master
FROM public.sales_enriched_v
GROUP BY
    sale_date,
    transaction_date,
    store_name,
    matched_product_id,
    COALESCE(jan_code, '-'),
    product_name,
    COALESCE(category, '未分類'),
    product_group,
    brand;
