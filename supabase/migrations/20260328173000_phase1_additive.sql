    ON public.product_aliases (alias_name, source_system, is_active);

ALTER TABLE public.product_aliases ENABLE ROW LEVEL SECURITY;

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_policies
        WHERE schemaname = 'public'
          AND tablename = 'product_aliases'
          AND policyname = 'Allow all access to product_aliases'
    ) THEN
        CREATE POLICY "Allow all access to product_aliases"
            ON public.product_aliases
            FOR ALL
            USING (TRUE)
            WITH CHECK (TRUE);
    END IF;
END $$;

